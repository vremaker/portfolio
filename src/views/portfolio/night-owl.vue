        students. This brings up the concern that walking near campus at night
        can be dangerous for students; students should not have to be concerned
        with their safety when walking around their university. This problem
        resonates with our team and peers, which is why we wanted to tackle the
        area of how to help students feel safe and confident walking near UW’s
        campus during night.”
      </p>

      <h3>INTERVIEWS</h3>
      <p>
        Our research consisted of three different methods: semi-structured
        interview, “fly on the wall” observation, and competitive analysis.
        Through these methods, we learned more about our user group and their
        goals and pain points as well as other products already on the market.
        Each team member conducted a one-on-one interview with a student at the
        University of Washington that walks during dusk or at night at least 4
        times a month. We then regrouped and synthesized our findings into key
        takeaways that would help us define our problem space and user group.
      </p>

      <h4>Key Takeaways:</h4>
