      <h3>PAPER PROTOTYPING USABILITY TESTING</h3>
      <p>
        The paper prototypes were a quick, easy way to test our designed
        features without distracting users with colors and photos. They helped
        us pinpoint confusing and unintuitive aspects of our initial features,
        which we incorporated into our redesign. First, we each individually
        sketched and described three interfaces. Then, we came back together and
        combined our rough draft sketches into consistent interface sketches for
        our usability tests. Based on our storyboards and information
        architecture diagram, we defined three key tasks that we asked users to
        complete in our tests.
      </p>
      <p>
        <em>Task 1:</em>User searches for a final destination, which will be
        home. Then they will select the path that they want to take and start
        their route.
      </p>
      <p>
        <em>Task 2:</em>User reports a burglary incident and inputs the correct
        information (date, time, description) and submits it. After submitting,
        check that it was successfully added to the map.
      </p>
      <p><em>Task 3:</em>User adds new safety contact and then calls them.</p>
      <h4>Participants:</h4>
      <p>
        We tested our prototype with four female University of Washington
        students who walk alone at night at least once a week.
      </p>
      <h4>Method:</h4>
      <p>
        For each evaluation, we followed three major phases of testing in order
        to cohesively gather quality information about our users and our app.
        The first phase started off with an interview with each participant in
        order to gather basic demographic information. The second phase
        consisted of the main usability testing by providing users with a task
        that they had to accomplish while observers recorded notes. During the
        test, we asked participants to think out loud so that we could better
        understand their thought process and pain points. To make it seem like a
        real application, we used a Marvel App to conduct our usability testing.
        Lastly, we performed a post test interview to get final details of what
        participants thought of their experience of our solution to night
        safety. After individually completing our usability tests, we came back
        together to synthesize and analyze our findings. We found commonalities
        in the features users struggled with, strengths and weaknesses of our
        design, and suggestions for redesign.
      </p>
