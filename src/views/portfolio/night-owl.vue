      <h2>HIGH FIDELITY PROTOTYPE</h2>
      <p>
        Based on all our research and testing, we created an app called Night
        Owl, that students could use to feel safe when walking alone at night.
        The app has two main components: safety circle and map.
      </p>
      <p>
        In the safety circle, based on interviewees stating they feel
        comfortable when they’re walking and talking to their friend, users are
        able to quickly and easily call their friends. If their friends are
        busy, users can call the bot. The bot will record and talk to them,
        mimicking a ‘friend’ listening to you in case your are in a dangerous
        situation.
      </p>
      <p>
        On the map, users can select a destination and the safest path to get
        there. Once the user starts their route, their safety contacts will be
        alerted and the app will automatically send another notification when
        the user gets to their final destination; this happens automatically
        because it’s easy to forget to let your friends know you got home safe.
        To keep the incidents up to date on the map, we use crowdsourcing; users
        can report and confirm hazards.
      </p>
      <p>
        After user feedback, we made the SOS button be easily clickable, which
        brings up a popup that counts down from 5 seconds and then calls 911. If
        the user accidently pressed the SOS button, they can easily cancel it.
        But we learned that it’s important the SOS button be very quick to use
        in case of a dire situation.
      </p>
      <p>
        For our colors, we chose dark mode because users would be using this app
        at night, so we wanted the screen to be subdued. Our color scheme is
        dark blue, bright orange, and bright red. It is important to create high
        contrast to show key information. Orange represents important actionable
        items such as reporting a hazard and starting route. The bright red is
        used only for the SOS button to ensure it stands out in case of
        emergency.
      </p>
