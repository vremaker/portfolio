        functions is the SOS button; we had gotten a lot of feedback about how
        to make it easily accessible while not making it easy to accidentally
        trigger. Based on user feedback, we were able to incorporate a solution
        into our final design, but we would want to test with more users.
        However, it was difficult to actually test the true functionality of the
        SOS button because it’s meant to be used in emergency situations.
        Another feature that we were unsure about was the ability to call a Bot.
        Some of our users said that they would feel weird talking to a Bot and
        wouldn’t use it, while some said they would. With more time, we would’ve
        wanted to figure out how to make this feature truly useful to our users.
      </p>
      <p>
        One really interesting question we received was about our research into
        how often our users were going between the same two points every week,
        and how we can optimize our map function to those users. This question
        was very thought-provoking; since one of our features was pathfinding on
        a map, we immediately turned to pre-existing solutions (e.g. Google
        Maps, Apple Maps) as a base. However, these solutions are made to deal
        with someone starting from anywhere and going to anywhere. Dealing with
        the situation of users going between the same two points multiple times
        a week might result in a completely different solution. If we had the
        time, we would want to explore optimization for this specific situation.
      </p>
