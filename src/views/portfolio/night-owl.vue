        <li>
          Users did not expect the app to immediately call a contact after
          clicking on their contact tile.
        </li>
        <li>
          Users want to be able to quickly input information when reporting a
          hazard.
        </li>
        <li>
          Users stated that hazards have different urgency levels, which should
          be reflected in the app.
        </li>
