    <h2>METHODS</h2>
    <p>
      This study consisted of two field observations as well as two interviews.
      Each field observation had the same main goal of understanding how
      students set up their rooms, a space where they are now spending almost
      all their time due to the shift that made classes entirely virtual.
      Because Gus was the only entirely asynchronous student in this sample and
      he was uncomfortable with me observing him while working asynchronously, I
      was only able to conduct a basic observation about the space he takes his
      asynchronous classes from. This included quiet observation of the room, as
      well asking him a couple of questions about specific items and oddities I
      noticed. During this observation, I sat on Gus’s desk chair which had been
      made from an old car seat, to get a basic understanding of his space. Kai,
      on the other hand had synchronous classes, which allowed me to sit in and
      observe her while she was in a quiz section. Kai’s observation began with
      a general overview of the space she was in, while Kai waited for class to
      start. Then, when her class did not begin on time, because her quiz
      section TA did not show up, the observation switched to watching her as
      she tried to figure out where to attend/watch class another way. During
      this observation, I sat on her bed and mostly stayed out of the way,
      asking an occasional clarification question. Although the flow for each
      observation was slightly different, the field notes were taken in a
      similar fashion. While observing I took jottings and short notes in a pink
      a4 notebook. Beside me, I had my stopwatch running, so that I could mark
      the time approximately every 5 minutes to understand the ebb and flow of
      these observations. Then, I translated the jottings into ethnographic
      field notes.
    </p>
    <p>
      Unlike the two unique field observations, the two interviews followed the
      same semi structured interview protocol. These interviews took place over
      Zoom and were recorded using their cloud recording feature. The interview
      consisted of questions based in four sections. It began with questions
      about college students including questions about what the student is
      studying, college year, number of credits, as well as their goals and
      motivations. Next, there were questions about synchronous classes
      including background information about the format of the online classes,
      the benefits and drawbacks, and stories from their virtual classroom
      experience. It then transitioned into more details about attending virtual
      classes including descriptions of what it looks like to attend virtual
      class for each student, signals and prompts that cause a student to engage
      or participate in class, and how the expert ace of class hand change with
      the switch to online classes. We concluded the interview by discussing how
      students focused in virtual class including recounting times when they
      have felt both focused and unfocused, changes in focus with the switch to
      online classes, what type of distractions they encounter, and what the
      biggest obstacles to focus have been. After each interview, I read through
      the transcripts and made them readable, so that they would be ready for
      analysis.
    </p>
    <p>
      Using a modified grounded theory approach, I began my data analysis. Using
      my research question “how do college students motivate and maintain
      productivity while learning from home?” as a guiding light, I read through
      my observational field notes from Kai and Gus as well as my interview
      transcripts from Travis and Victoria and began to make codes. I ended up
      with codes in three main categories, each with a set of subcodes. First
      there is the General details about participants and their space which
      consists of the codes: workstation detail, class detail, participants
      background, work versus relax areas. Second there is focus (or lack
      thereof) and meeting class goals, which consists of the codes: methods of
      focus, mental barrier to focus, accountability for getting work done,
      distraction from focus, feelings about being distracted. Finally, there is
      virtual classroom culture and how it differs from in person, which
      consists of the codes: participation, breakout room variation, technical
      difficulties, frustration, online teaching skill level, pre-recorded
      lecture habits, zoom camera etiquette and habits, class community
      building, exceptions made because of the pandemic, and class attendance
      changes. These codes were then used to create memos about general themes
      that were found in the data. These memos were turned into the findings
      included in the next section.
    </p>
    <h2>FINDINGS</h2>
    <p>
      Through coding and analysis, four main themes were discovered: separation
      of spaces, decrease in accountability, widespread zoom burnout, and the
      varying degrees of virtual participation.
    </p>
    <h3>Separation of Spaces</h3>
    <p>
      The biggest factor to consider when thinking about the space a pandemic
      student occupies is how they attempt to create a separation between their
      “working spaces,” where they are productive and get things done, and their
      “relaxation spaces,” where they can decompress and rest. Each student had
      distinct areas in which they relaxed or were productive, but these spaces
      were manifested in different ways. Gus, for example, has “two chairs: a
      chair for focused work and a more comfortable chair for gaming and
      relaxing. The chair that normally faces his monitors is a relaxation
      chair.” (Gus, 222–223) Gus also has his monitors set up so that “they
      could very easily be used as a TV with a good view from the bed.” (Gus,
      260–261) Kai on the other hand had two tapestries that “seem to serve as a
      bit of a divider for her bedroom, as they cover all the wall above where
      the bed touches the wall.” (Kai 217- 219) She also has a galaxy light
      simulator that sits on her dresser. She likes to turn it on when she is
      relaxing in bed. (Kai 231–2) Victoria has designated her bed as her
      relaxation space. Usually, when she is in classes, she tries to stay
      engaged in a chair with her camera on, but when she is feeling super tired
      and does not want to participate, she will sit in her bed with her camera
      off. (Victoria 133–135) Thus, students transformed a place that used to be
      mainly for relaxation into a space where they spend almost all their time
      due to the shelter in place orders set by the coronavirus pandemic.
    </p>
    <h3>Decrease in Accountability</h3>
    <p>
      Students note that although they had distractions during in person
      classes, the ways in which they are distracted has shifted dramatically
      with the move to online learning. One student, Travis, noted that he used
      to fall asleep a fair amount during in person classes (only for a few
      minutes at a time, sometimes multiple times a day), but since the move to
      online classes, he finds that he no longer sleeps because he is distracted
      by a lot more things such as homework for his other classes, Reddit and
      YouTube. (Travis 64) Travis noted that because he had three monitors, his
      intense distractions during class was camouflaged. Even when he was not
      paying very much attention to the class. He slyly claimed, “when I’m
      looking at other shit going on, [I’m] distracted, but it looks like I’m
      focusing at the same time. So that’s the beauty of it.” (Travis 160–162)
      This led Travis to take less notes during online classes. (Travis 168) It
      is very difficult to gauge productivity and focus when you are looking at
      a blank square with their name in it, or at a tiny video feed of just
      their face. The virtual world has fewer physical indicators of focus, and
      they are hard to see; this creates far less accountability towards
      actually paying attention in virtual class. Another student, Victoria,
      noted that in person, she used to not use her phone when she was in
      smaller classes because she is terrified of authority, but now, during
      virtual classes, she finds herself using her phone more even in the
      smaller classes because she “feels there’s way less consequences now.” She
      also noted that she’s on her phone during class because she wants to be
      doing anything but learning from home (Victoria 141–147) Although she is
      more likely to show up to all of her classes because attending class is as
      simple as clicking a button, she finds that she spends more time on her
      phone during class.
    </p>
    <h3>Zoom Burnout</h3>
    <p>
      Each student has different ways of keeping up with their schoolwork and
      remaining productive while feeling the burnout involved in having entirely
      virtual classes. Gus gets his motivation from the creation of clean
      workspaces. When he looks at his desk he thinks, “that is a proper desk”
      and it motivates him to get work done. (Gus 187–189) Gus also uses a
      stimulant called nuvigil to help him focus, because he has ADHD (Gus 235).
      Kai keeps focused by using a pile of small scraps of paper, which she used
      to keep track of the work that she has due for all her classes. Each slip
      represents a different class, and she lists the assignments on the lines
      below.” (Kai 186–189) Despite keeping up with their assignments, student
      engagement in classes is way down, probably because all classes take place
      in essentially the same space, a student’s computer, rather than being
      located in different buildings all across campus as is the case when
      classes are in person. Not one, but two participants from this study
      struggled to fully remember their class schedule even though I talked with
      them during the fifth week of a ten-week quarter, a point where, had the
      classes been in person their schedules would have been fully memorized.
      Travis could not list all of the classes he was taking without looking at
      his canvas page. (Travis 32) Victoria had forgotten about a weekly class
      meeting because it had been cancelled for the past two weeks for various
      reasons including election stress and an exam. (Victoria 36–37) This is a
      clear sign of the burnout that students are facing as online synchronous
      classes continue to be the only way to attend class during this pandemic.
      This burnout also occasionally affected class attendance. Victoria, for
      example, would leave classes prematurely if she felt particularly burned
      out, due to being stressed out by other things, or if she was having a bad
      mental health day where she could not stand to be at her computer anymore
      (Victoria 71–75) If these classes were in person, there would be some
      motive (and a change of scenery), but in the modern era of virtual
      learning, it feels like I must sit through it and the phone is a ready
      distraction to help alleviate the pain and stress of being stuck at your
      computer for school all day every day (Victoria 149–151).
    </p>
    <h3>Decrease in Participation</h3>
    <p>
      The consensus for the experiences in online classes is that they are not
      as interactive, because there are less opportunities to interact with
      other students. (Travis 62–3) The switch from on campus learning to
      holding classes entirely though video conferencing software has wrought
      havoc on class participation. Many students taking online classes never
      have their camera on, and some only turn it on for short periods of time.
      In most classes there are typically under five students with their camera
      on, no matter how large the class is. Yet, in some classes, the only
      person with their camera on is the professor. For Travis, “sometimes, no
      one turns their camera on or participates, it’s only me, but in their
      classes, more people turn the cameras, which is kind of nice.” (Travis 95)
      Travis stated that “I feel bad for the instructors sometimes when they are
      staring at a blank board. Sometimes it’s easier to interact and have a
      productive class session that mimics the tradition classroom experience by
      having my camera on” (Travis 100–103) For Victoria, in her “smaller
      classes, it’s me and like one or two other people who will turn on their
      camera, but most people are willing to participate.” (Victoria, 42–45)
      This highlights a strange new culture in the classroom, students who are
      too shy to participate with their video on or to use their voice but are
      willing to participate through the chat. Victoria finds that although in
      the main session most cameras are off, “in breakout rooms, most people do
      turn on their cameras, and I keep mine on. I participate a lot because no
      one else really does.” (Victoria, 88–91) As people become more accustomed
      to Zoom (and probably more desperate for social interactions), the culture
      around Zoom becomes more solidified in the concept that “if you are in a
      breakout room, you unmute yourself and turn on your camera. And I think
      that that’s like becoming a little bit more universal and that’s made it
      easier as well.” (Victoria 128–130) Both Travis and Victoria are motivated
      to have their camera on for similar reasons, to replicate the real Zoom
      experience. I find it strange that the two people that I interview seem to
      have their camera on a lot, yet most students still keep their cameras off
      for the majority of synchronous classes. Students seem to have two main
      motivators for participating and focusing during online classes. The first
      being the fact that it is “still paying full tuition for the classes for
      basically doing Khan Academy online. So, I might as well get the most of
      my learning experience.” (Travis, 133–135) The second being out of respect
      for the professors, because, again, students are paying to be there to
      learn from these professors. (Victoria 54)
    </p>
    <h2>DESIGN IMPLICATIONS</h2>
    <p>
      More than anything, the findings of this study show how truly desolate the
      social arena looks for college students during the pandemic. Normally
      college life is typically filled with some of the most social interactions
      in a student’s developmental life. The decrease in social interactions,
      which impacts student focus, seems to hinge solely on the new limitations
      of the era: being stuck in your house due to the coronavirus pandemic, as
      well as the limitations of engagement that video conferencing services
      provide. These struggles highlight a key area for design improvement: how
      to make virtual interactions feel more real, and how to encourage student
      social interaction despite the barriers presented by online learning in
      the pandemic. This is a huge design opportunity for software industry
      professionals as they continue to create software and programs that will
      facilitate face to face interactions despite not being together in person.
      Some solutions such as Remo aim to mimic the in-person space by creating a
      bit of an RPG Pokémon-esque experience. This still utilizes video
      conferencing though which still limits the degree to which you can
      interact with other people and participants. Based on the truly astounding
      burnout that students are feeling towards Zoom, as well as the new
      standards of attending big lectures without your video on, the methods
      that are based solely on interaction over video camera seem to be a bit
      naïve. By understanding student focus participation and distraction,
      engineers can attempt to build classroom environments for better focusing
      for students.
    </p>
    <p>
      This research could also be used to inform the creation of curricula for
      future online classes, because it is very apparent that we probably will
      not be back together on campus until at least spring quarter 2021, but
      more realistically, students will not be back on campus until the fall of
      2021. This research on what is distracting to students, and how students
      handle their focusing while learning from home, could be vital in making
      changes and accommodations to current class formats in order to be better
      suited for the online world. This could be through changing the class
      format from asynchronous to synchronous, changing the type of projects and
      assessments being given to students, and changing the way that students
      interact, or even by creating new methods and forums for class
      participation.
    </p>
    <h2>CONCLUSION</h2>
    <p>
      The swift and disruptive changes that rocked the world at the start of the
      novel coronavirus Pandemic had caused intense drastic change in the way
      that college students attend class. Students moved from in person classes
      to virtual classes to taking either asynchronous classes or synchronous
      classes online from their home. Starved of the social and physical
      interactions of normal college life, student morale and mental health is
      very low, yet students are still chugging along towards their degree,
      which led the study to explore this question: how do college students
      motivate and maintain productivity while learning from home? This study
      found that focus and learning from home was drastically impacted by four
      factors: the way in which student separated the space in their home to
      have distinct areas for study and focus as well as relaxation and
      decompression, as well as the decrease in accountability that students
      feel during virtual classes, focusing during class while it is online,
      changes in what it means to participate in class, and the large effects of
      zoom burnout. These barriers to focus seem to hinge on the new limitations
      of the era: being stuck in your house due to the coronavirus pandemic, as
      well as the limitations of engagement that video conferencing services
      provide.
    </p>
    <p>
      The biggest limiting factor of this study is the small sample size. These
      limited results make the overarching results of this study less
      generalizable. This study may also have been slightly limited using Zoom
      as my main platform for interviewing, especially because the physical
      space seems to have a large impact on the productivity of a student while
      learning from home. Although I am content with my research question, in
      further iterations of this study, my interview protocol would contain more
      questions about the separation of space. Because way that space is divided
      seemed to have a very large impact on mental health, which is a huge
      factor in pandemic focus. I would also increase the number of participants
      and solidify the field observation protocol so that it is only while the
      student is getting work done for a particular class.
    </p>
