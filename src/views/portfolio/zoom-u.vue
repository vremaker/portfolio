    <h2>INTRODUCTION</h2>
    <p>
      The Novel Coronavirus Pandemic led to swift and disruptive changes to the
      everyday lives of people all around the world. For most colleges, the
      Spring of 2020 marked the shift to online learning. Students left campus
      for spring break and just never came back. Colleges choose to move classes
      to an entirely virtual platform consisting of both asynchronous and
      synchronous classes to keep as many people safe as possible from this
      deadly virus. Many students both asynchronous and synchronous classes in
      their schedule. The main difference between these two class formats is
      that synchronous classes have a set time where students and the professor
      got together in a video conference of some sort, typically Zoom, and have
      lecture and section live. Asynchronous classes, on the other hand, have no
      set meeting time and students are given weekly assignments that they
      complete at their own pace.
    </p>
    <p>
      On top of the physical and emotional stress of living through a global
      pandemic, the shift to virtual learning took a huge portion of the college
      experience away from students. Instead of physical classes, where students
      could interact with their fellow classmates before, during, and after
      class; students now spend countless hours sitting in front of their
      computers, where the only real social interactions take place in breakout
      rooms and over class message boards. Starved of the social and physical
      interactions of normal college life, student morale and mental health is
      very low, leading to a distinct lack of motivation and productivity. Yet,
      students are still chugging along towards their degree. This begs the
      question: how do college students motivate and maintain productivity while
      learning from home?
    </p>
